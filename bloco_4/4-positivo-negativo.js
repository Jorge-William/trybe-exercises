/**
 * Faça um programa que, dado um valor definido numa variável, retorne "positive" 
 * se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.
*/
var resposta = -99;


if(resposta > 0){
    console.log("Positivo");
} else if( resposta < 0){
    console.log("Negativo");
} else {
    console.log(0);
}





